# ── Test helpers ─────────────────────────────────────────────────────────────

PASSED=0
FAILED=0

check() {
    local desc="$1" pattern="$2" output="$3"
    if echo "$output" | grep -q "$pattern"; then
        echo "  [PASS] $desc"
        PASSED=$((PASSED + 1))
    else
        echo "  [FAIL] $desc  (expected pattern: '$pattern')"
        echo "         actual: $(echo "$output" | head -3 | tr '\n' '|')"
        FAILED=$((FAILED + 1))
    fi
}

check_not() {
    local desc="$1" pattern="$2" text="$3"
    if ! echo "$text" | grep -q "$pattern"; then
        echo "  [PASS] $desc"
        PASSED=$((PASSED + 1))
    else
        echo "  [FAIL] $desc  (unexpected pattern found: '$pattern')"
        echo "         actual: $(echo "$text" | grep "$pattern" | head -2 | tr '\n' '|')"
        FAILED=$((FAILED + 1))
    fi
}

# Create an isolated HOME directory with one account config.
#   make_home <home_path> <email_address> <imap_port>
make_home() {
    local home="$1" email="$2" port="$3"
    case "$home" in
        ./build/tests/functional/homes/*) ;;
        *) echo "ERROR: make_home: refusing unsafe path '$home'" >&2; exit 1 ;;
    esac
    rm -rf "./build/tests/functional/homes/${home##./build/tests/functional/homes/}"
    mkdir -p "$home/.config/email-cli/accounts/$email"
    cat > "$home/.config/email-cli/accounts/$email/config.ini" <<CONFIG
EMAIL_HOST=imaps://localhost:$port
EMAIL_USER=$email
EMAIL_PASS=testpass
EMAIL_FOLDER=INBOX
EMAIL_SENT_FOLDER=INBOX.Sent
SSL_NO_VERIFY=1
SMTP_HOST=smtps://localhost:$SMTP_PORT
SMTP_USER=$email
SMTP_PASS=testpass
CONFIG
}

# Add a second account config to an existing HOME directory.
#   add_account <home_path> <email_address> <imap_port>
add_account() {
    local home="$1" email="$2" port="$3"
    mkdir -p "$home/.config/email-cli/accounts/$email"
    cat > "$home/.config/email-cli/accounts/$email/config.ini" <<CONFIG
EMAIL_HOST=imaps://localhost:$port
EMAIL_USER=$email
EMAIL_PASS=testpass
EMAIL_FOLDER=INBOX
EMAIL_SENT_FOLDER=INBOX.Sent
SSL_NO_VERIFY=1
CONFIG
}

# Run `email-cli list --batch [--folder F]` with the given HOME and optional
# shared XDG_DATA_HOME; returns stdout+stderr combined.
run_list() {
    local home="$1" data_home="$2" folder="${3:-INBOX}"
    local args=("--batch" "list")
    [ "$folder" != "INBOX" ] && args+=("--folder" "$folder")
    (export HOME="$home"
     [ -n "$data_home" ] && export XDG_DATA_HOME="$data_home" || unset XDG_DATA_HOME
     unset XDG_CONFIG_HOME XDG_CACHE_HOME
     "$BIN_DIR/email-cli" "${args[@]}" 2>&1 || true)
}

# Run `email-cli <account> list` — explicit account selection (multi-account homes).
run_list_as() {
    local account="$1" home="$2" data_home="$3" folder="${4:-INBOX}"
    local args=("$account" "--batch" "list")
    [ "$folder" != "INBOX" ] && args+=("--folder" "$folder")
    (export HOME="$home"
     [ -n "$data_home" ] && export XDG_DATA_HOME="$data_home" || unset XDG_DATA_HOME
     unset XDG_CONFIG_HOME XDG_CACHE_HOME
     "$BIN_DIR/email-cli" "${args[@]}" 2>&1 || true)
}

# Run `email-cli show <uid>` with the given HOME and optional XDG_DATA_HOME.
run_show() {
    local home="$1" data_home="$2" uid="${3:-1}"
    (export HOME="$home"
     [ -n "$data_home" ] && export XDG_DATA_HOME="$data_home" || unset XDG_DATA_HOME
     unset XDG_CONFIG_HOME XDG_CACHE_HOME
     "$BIN_DIR/email-cli" show "$uid" 2>&1 || true)
}

# Run `email-cli folders` with the given HOME.
run_folders() {
    local home="$1" data_home="$2" extra="${3:-}"
    (export HOME="$home"
     [ -n "$data_home" ] && export XDG_DATA_HOME="$data_home" || unset XDG_DATA_HOME
     unset XDG_CONFIG_HOME XDG_CACHE_HOME
     # shellcheck disable=SC2086
     "$BIN_DIR/email-cli" list-folders $extra 2>&1 || true)
}

